function zox_after_import_setup() {
    // Assign menus to their locations.
    $main_menu = get_term_by( 'name', 'Main Menu', 'nav_menu' );
    $sec_menu = get_term_by( 'name', 'Secondary Menu', 'nav_menu' );
    $foot_menu = get_term_by( 'name', 'Footer Menu', 'nav_menu' );

    set_theme_mod( 'nav_menu_locations', array(
            'main-menu' => $sec_menu->term_id,
            'sec-menu' => $sec_menu->term_id,
            'mobile-menu' => $sec_menu->term_id,
            'footer-menu' => $sec_menu->term_id,
        )
    );

    // Assign front page and posts page (blog page).
    $front_page_id = get_page_by_title( 'Home' );

    update_option( 'show_on_front', 'page' );
    update_option( 'page_on_front', $front_page_id->ID );

}
add_action( 'pt-ocdi/after_import', 'zox_after_import_setup' );

